package com.sist.main;

public class CampingVO {

    private int camp_id;
    private String camp_name;
    private String camp_address;
    private String camp_type;
    private String camp_content;
    private String camp_photo;

    public int getCamp_id() {
        return camp_id;
    }

    public void setCamp_id(int camp_id) {
        this.camp_id = camp_id;
    }

    public String getCamp_name() {
        return camp_name;
    }

    public void setCamp_name(String camp_name) {
        this.camp_name = camp_name;
    }

    public String getCamp_address() {
        return camp_address;
    }

    public void setCamp_address(String camp_address) {
        this.camp_address = camp_address;
    }

    public String getCamp_type() {
        return camp_type;
    }

    public void setCamp_type(String camp_type) {
        this.camp_type = camp_type;
    }

    public String getCamp_content() {
        return camp_content;
    }

    public void setCamp_content(String camp_content) {
        this.camp_content = camp_content;
    }

    public String getCamp_photo() {
        return camp_photo;
    }

    public void setCamp_photo(String camp_photo) {
        this.camp_photo = camp_photo;
    }
}